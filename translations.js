function updateHeaderFlag(lang) {
    const flagEl = document.getElementById('current-flag-emoji');
    if (flagEl && FLAG_MAP[lang]) {
        flagEl.textContent = FLAG_MAP[lang];
    }
    const selectEl = document.getElementById('header-lang-select');
    if (selectEl) {
        selectEl.value = lang;
    }
}

